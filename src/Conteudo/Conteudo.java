package Conteudo;

public class Conteudo {
    private String nome;
    private String DtCriacao;
    private String UrlConteudo;
    private String UrlImagem;



    public Conteudo(String nome, String dtCriacao, String urlConteudo, String urlImagem) {
        this.nome = nome;
        DtCriacao = dtCriacao;
        UrlConteudo = urlConteudo;
        UrlImagem = urlImagem;
    }
}
